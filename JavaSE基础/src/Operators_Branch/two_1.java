package Operators_Branch;
/*
*  算术运算符简单实操
*
*
* */
public class two_1 {
    public static void main(String[] args) {
        int a = 100;
        int b = 20;
        int c = 30;
        //基本的加减法
        System.out.println("相减之后");
        System.out.println( a - b);
        System.out.println("相加之后");
        System.out.println(a + b);
        System.out.println("相加之后");
        System.out.println(a + c);
        System.out.println("相减之后");
        System.out.println(a - c);
        //乘除法
        System.out.println("相除之后" + a / b);
        System.out.println("相乘之后" + a * b);

    }
}
