package Operators_Branch;

public class two_5 {
    //关系运算符
    public static void main(String[] args) {
        //定义变量
        int i = 10;
        int x = 10;
        int y = 20;
        //输出结果为true或者false
        System.out.println(i == x);
        System.out.println(i == y);

        System.out.println(i != x);
        System.out.println(i != y);

        System.out.println(i > x);
        System.out.println(i > y);

        System.out.println(i >= x);
        System.out.println(i >= y);

        System.out.println(i < x);
        System.out.println(i < y);

        System.out.println(i <= x);
        System.out.println(i <= y);

        //一个等于号为赋值
        System.out.println(i = y);



    }
}
