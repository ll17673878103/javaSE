package Cycle;

public class timeDemo {
    public static void main(String[] args) {
        for (int i = 0; i < 4; i++) {
            for (int x = 0; x < 3; x++) {
                System.out.println(i + "小时" + x + "分钟");
            }
            System.out.println("----------");
        }
    }
}
